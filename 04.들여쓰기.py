# 들여쓰기 (indent)
# - 들여쓰기는 코드를 읽기 쉽도록 일정한 간격을 띄워서
#   작성하는 방법, 특히 파이썬은 들여쓰기 자체가 문법
a = 1 # a는 1이다
if a == 10: # a가 10인가? (조건문)
# print("a는 10입니다") # if문 다음에 들여쓰기가 되어 있어야 정상 작동한다
    print("a는 10입니다") # if문 다음에 들여쓰기가 되어 있어야 정상 작동한다
# a가 10인지 여부에 따라서 작동 여부를 결정할 수 있는데...
# 10일 때 실행하는 구문을 -> 들여쓰기를 통해서 정할 수 있다